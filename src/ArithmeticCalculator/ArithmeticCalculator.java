package ArithmeticCalculator;

public class ArithmeticCalculator {
    private int first;
    private int second;

    public ArithmeticCalculator(int first, int second) {
        this.first = first;
        this.second = second;
    }

    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }

}
